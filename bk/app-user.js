const state = {
    timelineData: [],
    modelsData: [],
    currentYear: 2005,
    currentBrand: "Bosch",
    selectedModel: null,
    // Filter states (connected to header dropdowns)
    filters: {
        reportType: 'year-report',  // 'timeline' | 'year-report'
        toolType: 'miter-saw',      // 'miter-saw' | 'table-saw'
        region: 'na'                // 'na' | 'eu' | 'jp'
    }
};

// Brand → Region Mapping
const BRAND_REGION_MAP = {
    na: ['DeWalt', 'Milwaukee', 'Ridgid', 'Kobalt', 'Delta'],
    eu: ['Bosch', 'Festool', 'Metabo', 'Einhell', 'Scheppach'],
    jp: ['Makita', 'MetaboHPT - Hitachi']
};

// Get brands for current region filter
function getBrandsForRegion(region) {
    return BRAND_REGION_MAP[region] || Object.values(BRAND_REGION_MAP).flat();
}

// Filter models by region and year
function filterModels(models, region, year) {
    const regionBrands = getBrandsForRegion(region);
    return models.filter(m => {
        const brandMatch = regionBrands.includes(m.Brand);
        const yearMatch = parseInt(m['Released Year']) === year;
        return brandMatch && yearMatch;
    });
}

const BrandColors = {
    "Bosch": "#EC8282",
    "Makita": "#00B0F0",
    "DeWalt": "#FFC000",
    "Milwaukee": "#FF0000",
    "Festool": "#99CC00"
};

document.addEventListener("DOMContentLoaded", () => {
    init();
});

function init() {
    if (typeof Papa === "undefined") {
        console.error("PapaParse not loaded, waiting...");
        setTimeout(init, 100); // Retry
        return;
    }

    // Parse Embedded CSVs
    const timelineResults = Papa.parse(TIMELINE_CSV, { header: true, skipEmptyLines: true });
    state.timelineData = timelineResults.data;

    const modelsResults = Papa.parse(MODELS_CSV, { header: true, skipEmptyLines: true });
    state.modelsData = modelsResults.data;

    setupEventListeners();
    renderSidebar();
    updateView();
}

function setupEventListeners() {
    const upBtn = document.getElementById("nav-up");
    if (upBtn) upBtn.addEventListener("click", () => changeYear(-1));

    const downBtn = document.getElementById("nav-down");
    if (downBtn) downBtn.addEventListener("click", () => changeYear(1));

    const sourceSelect = document.getElementById("source-select");
    if (sourceSelect) {
        sourceSelect.addEventListener("change", (e) => {
            state.currentBrand = e.target.value;
            updateView();
        });
    }

    // Dropdown Menu Handlers
    document.querySelectorAll(".dropdown").forEach(dropdown => {
        const label = dropdown.querySelector(".dropdown-label");
        const options = dropdown.querySelectorAll(".dropdown-option");

        options.forEach(option => {
            option.addEventListener("click", (e) => {
                e.stopPropagation();

                // Update label text
                if (label) label.textContent = option.textContent;

                // Update active state
                options.forEach(o => o.classList.remove("active"));
                option.classList.add("active");

                // Update state based on dropdown ID
                const dropdownId = dropdown.id;
                const value = option.dataset.value;

                switch (dropdownId) {
                    case 'menu-report':
                        state.filters.reportType = value;
                        break;
                    case 'menu-tool':
                        state.filters.toolType = value;
                        break;
                    case 'menu-region':
                        state.filters.region = value;
                        // Update currentBrand to first brand in new region
                        const regionBrands = getBrandsForRegion(value);
                        if (regionBrands.length > 0) {
                            state.currentBrand = regionBrands[0];
                        }
                        break;
                }

                console.log(`Filter updated: ${dropdownId} = "${value}"`);
                updateView();
            });
        });
    });
}

function renderSidebar() {
    const list = document.getElementById("year-list");
    if (!list) return;
    list.innerHTML = "";

    for (let y = 2005; y <= 2025; y++) {
        const btn = document.createElement("button");
        btn.className = `year-btn ${y === state.currentYear ? "active" : ""}`;
        btn.textContent = y;
        btn.onclick = () => {
            state.currentYear = y;
            updateView();
            renderSidebar(); // Update active state visually (re-render easiest)
        };
        list.appendChild(btn);
    }

    // Scroll to active
    setTimeout(() => {
        const activeBtn = list.querySelector(".active");
        if (activeBtn) activeBtn.scrollIntoView({ block: "center", behavior: "smooth" });
    }, 50);
}

function changeYear(offset) {
    const newYear = state.currentYear + offset;
    if (newYear >= 2005 && newYear <= 2025) {
        state.currentYear = newYear;
        updateView();
        renderSidebar();
    }
}

function updateView() {
    // 1. Brand Title & Color
    const title = document.getElementById("brand-title");
    if (title) title.textContent = state.currentBrand.toUpperCase();

    const root = document.documentElement;
    const color = BrandColors[state.currentBrand] || "#EC8282";
    root.style.setProperty("--brand-color", color);

    // 2. Timeline Data
    const timelineEntry = state.timelineData.find(d =>
        parseInt(d["Year"]) === state.currentYear &&
        d["Brand"].toLowerCase() === state.currentBrand.toLowerCase()
    );

    const topDesc = document.getElementById("year-desc-text");
    const bottomDesc = document.getElementById("bottom-desc-box");
    const bottomText = document.getElementById("bottom-desc-text");

    if (topDesc) {
        if (timelineEntry) {
            topDesc.innerHTML = `<strong>In ${state.currentYear},</strong> ` + (timelineEntry["Market_Notes"] || "No data available.");

            if (bottomDesc && bottomText) {
                if (timelineEntry["Tech_Notes"]) {
                    bottomDesc.style.display = "block";
                    bottomText.innerHTML = timelineEntry["Tech_Notes"];
                } else {
                    bottomDesc.style.display = "none";
                }
            }
        } else {
            topDesc.textContent = `Data not found for ${state.currentBrand} in ${state.currentYear}.`;
            if (bottomDesc) bottomDesc.style.display = "none";
        }
    }

    // 3. Models Data
    const models = state.modelsData.filter(m =>
        parseInt(m["Released Year"]) === state.currentYear &&
        m["Brand"].toLowerCase() === state.currentBrand.toLowerCase()
    );

    // Update New Model Tag visibility
    const newModelGroup = document.querySelector(".new-model-group");
    if (newModelGroup) {
        newModelGroup.style.opacity = models.length > 0 ? "1" : "0.3";
    }

    renderProductArea(models);
}

function renderProductArea(models) {
    const thumbGrid = document.getElementById("thumb-grid");
    if (!thumbGrid) return;
    thumbGrid.innerHTML = "";

    const featureImg = document.getElementById("feature-image");
    const specPanel = document.getElementById("spec-panel");

    if (models.length === 0) {
        if (featureImg) featureImg.src = "";
        if (specPanel) specPanel.innerHTML = '<p style="padding:10px; color:#666;">No models released.</p>';
        return;
    }

    // Auto-select first
    if (!state.selectedModel || !models.includes(state.selectedModel)) {
        state.selectedModel = models[0];
    }

    // Render Cards
    models.forEach(model => {
        const card = document.createElement("div");
        card.className = `thumb-card ${model === state.selectedModel ? "active" : ""}`;

        const img = document.createElement("img");
        img.src = model["Img_Path"] || "https://via.placeholder.com/100?text=No+Img";
        img.className = "thumb-img";

        card.appendChild(img);
        card.onclick = () => {
            state.selectedModel = model;
            updateView();
        };
        thumbGrid.appendChild(card);
    });

    // Render Specs
    if (specPanel) {
        specPanel.innerHTML = "";
        const m = state.selectedModel;
        const desc = (m["Review_Summary"] || "").toLowerCase();
        const name = (m["Model Name"] || "").toLowerCase();

        let slideType = "Fixed";
        if (desc.includes("glide")) slideType = "Glide";
        else if (desc.includes("rail") || desc.includes("slide") || name.includes("slide")) slideType = "Rail";

        let powerType = "Corded";
        if (name.includes("18v") || name.includes("36v") || desc.includes("cordless") || desc.includes("battery")) powerType = "Cordless";

        const specs = [
            { key: "Model", val: `# ${m["Model Name"] || "?"}` },
            { key: "Bevel", val: desc.includes("dual") ? "Dual" : "Single" },
            { key: "Slide", val: slideType },
            { key: "Power", val: powerType },
            { key: "Blade", val: m["Blade Diameter"] || "-" },
            { key: "Measure", val: desc.includes("laser") ? "Laser" : (desc.includes("shadow") ? "Shadow" : "-") },
            { key: "Soft Start", val: "Yes" },
            { key: "Others", val: "-" },
            { key: "Ref", val: '<a href="#" style="color:#000; text-decoration:none;">| Link |</a>' }
        ];

        specs.forEach(s => {
            const row = document.createElement("div");
            row.className = "spec-row";
            row.innerHTML = `<div class="spec-key">${s.key} :</div><div class="spec-val">${s.val}</div>`;
            specPanel.appendChild(row);
        });
    }

    // Render Feature Image
    if (featureImg) {
        featureImg.src = state.selectedModel["Img_Path"] || "";
    }
}
